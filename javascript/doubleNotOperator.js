// The !! (double NOT) operator is used in JavaScript to convert any value into a boolean (true or false).


console.log(!!1);        // true
console.log(!!0);        // false
console.log(!!"hello");  // true
console.log(!!"");       // false
console.log(!!null);     // false
console.log(!!undefined);// false
console.log(!!NaN);      // false
console.log(!!{});       // true
console.log(!![]);       // true
